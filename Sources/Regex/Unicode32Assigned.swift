    /// Whether `value` was an assigned code point as of Unicode 3.2. Binary
    /// search over ``assignedRanges``; each step compares one range's bounds.
    static func assignedInUnicode32(_ value: UInt32) -> Bool {
        var low = 0
        var high = assignedRanges.count / 2
        while low < high {
            let mid = (low + high) / 2
            if value < assignedRanges[mid * 2] {
                high = mid
            } else if value > assignedRanges[mid * 2 + 1] {
                low = mid + 1
            } else {
                return true
            }
        }
        return false
    }
